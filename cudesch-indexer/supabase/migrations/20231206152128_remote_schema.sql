CREATE OR REPLACE FUNCTION "public"."match_chapters"("query_embedding" "public"."vector", "match_count" integer DEFAULT 5, "filter" "jsonb" DEFAULT '{}'::"jsonb", "document_names" "jsonb" DEFAULT NULL::"jsonb") RETURNS TABLE("id" bigint, "content" "text", "metadata" "jsonb", "embedding" "jsonb", "similarity" double precision)
    LANGUAGE "plpgsql" SECURITY DEFINER
    AS $$
#variable_conflict use_column
begin
  return query
  select
    id,
    content,
    metadata,
    (embedding::text)::jsonb as embedding,
    1 - (chapters.embedding <=> query_embedding) as similarity
  from chapters
  where metadata @> filter
  and (document_names is null or document_names @> (chapters.metadata -> 'documentName'))
  order by chapters.embedding <=> query_embedding
  limit least(match_count, 50);
end;
$$;

